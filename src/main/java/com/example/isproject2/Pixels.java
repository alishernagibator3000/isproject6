package com.example.isproject2;

public class Pixels {
    private int r;
    private int g;
    private int b;

    public Pixels(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public void setR(int r) {
        this.r = r;
    }
    public int getR() {
        return r;
    }
    public void setG(int g) {
        this.g = g;
    }
    public int getG() {
        return g;
    }
    public void setB(int b) {
        this.b = b;
    }
    public int getB() {
        return b;
    }
}
